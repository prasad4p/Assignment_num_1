# Question_number_3 = write the python program to count the number of even and odd number from a series of number.


numbers = (3,8,11,19,27,30,35,48,55,74)
even_number = 0
odd_number = 0
for i in numbers:
        if not i % 2:
             even_number += 1
        else:
             odd_number += 1
print("Number of even numbers :" , even_number)
print("Number of odd numbers :" , odd_number )


   

  